const mongoose = require('mongoose');

const technicianSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
    unique: true
  },
  skills: [{
    type: String,
    required: true,
    enum: [
      'Plumber', 'Electrician', 'Mechanic', 'AC Technician',
      'Carpenter', 'Cleaner', 'Painter', 'Engineer',
      'Appliance Repair', 'Locksmith', 'Welder', 'Mason',
      'Pest Control', 'Gardener', 'Interior Designer'
    ]
  }],
  experience: {
    type: Number,
    required: [true, 'Experience is required'],
    min: [0, 'Experience cannot be negative']
  },
  chargeType: {
    type: String,
    enum: ['hourly', 'per_job'],
    default: 'hourly'
  },
  chargeRate: {
    type: Number,
    required: [true, 'Charge rate is required'],
    min: [0, 'Charge rate cannot be negative']
  },
  bio: {
    type: String,
    maxlength: [500, 'Bio cannot exceed 500 characters'],
    default: ''
  },
  serviceRadius: {
    type: Number,
    default: 10,
    min: 1,
    max: 100
  },
  availability: {
    isOnline: { type: Boolean, default: true },
    slots: [{
      day: {
        type: String,
        enum: ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday']
      },
      startTime: String,
      endTime: String
    }]
  },
  rating: {
    average: { type: Number, default: 0, min: 0, max: 5 },
    count: { type: Number, default: 0 }
  },
  completedJobs: { type: Number, default: 0 },
  totalEarnings: { type: Number, default: 0 },
  isVerified: { type: Boolean, default: false },
  documents: [{
    name: String,
    url: String
  }],
  gallery: [{ type: String }]
}, {
  timestamps: true
});

technicianSchema.index({ skills: 1 });
technicianSchema.index({ 'rating.average': -1 });
technicianSchema.index({ chargeRate: 1 });

module.exports = mongoose.model('Technician', technicianSchema);
